    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        Connection con = conn.abrir();
        
        Usuarios_objeto o = new Usuarios_objeto();
        Usuarios_interacao i = new Usuarios_interacao(con);
        
        o.setCPF(cpf.getText());
        o.setNome(nome.getText());
        o.setEmail(email.getText());
        o.setLogin(login.getText());
        o.setSenha(senha.getText());
        o.setTipo(tipo.getSelectedItem()+"");
        o.setTelefone(telefone.getText());
        verificar.setText(i.insertUsuario(o));
        conn.fechar(con);
    }//GEN-LAST:event_btnCadastrarActionPerformed

